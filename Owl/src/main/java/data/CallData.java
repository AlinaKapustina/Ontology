
package data;


public class CallData {
    
    private int minNumberCall;
    private int maxNumberCall;
    
    private int numberHomeAreaCall;
    private int numberOtherAreaCall;

    public int getMinNumberCall() {
        return minNumberCall;
    }

    public void setMinNumberCall(int minNumberCall) {
        this.minNumberCall = minNumberCall;
    }

    public int getMaxNumberCall() {
        return maxNumberCall;
    }

    public void setMaxNumberCall(int maxNumberCall) {
        this.maxNumberCall = maxNumberCall;
    }

    public int getNumberHomeAreaCall() {
        return numberHomeAreaCall;
    }

    public void setNumberHomeAreaCall(int numberHomeAreaCall) {
        this.numberHomeAreaCall = numberHomeAreaCall;
    }

    public int getNumberOtherAreaCall() {
        return numberOtherAreaCall;
    }

    public void setNumberOtherAreaCall(int numberOtherAreaCall) {
        this.numberOtherAreaCall = numberOtherAreaCall;
    }
    
    
    
}
